#include "libft.h"

void	ft_putnbr(int n)
{
	int div;

	div = 1;
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		ft_putchar('-');
		n *= -1;
	}
	while ((n / div) >= 10)
		div *= 10;
	while (div > 0)
	{
		ft_putchar((n / div) % 10 + 48);
		div /= 10;
	}
}
